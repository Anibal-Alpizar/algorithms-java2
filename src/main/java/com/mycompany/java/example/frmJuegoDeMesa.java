    private void inicializarTabla() {
        modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("ID");
        modeloTabla.addColumn("Descripci√≥n");
        tblProveedores.setModel(modeloTabla);
    }

    private void cargarComboProveedores() {
        List<Proveedor> proveedores = BLLProveedor.obtenerTodosLosProveedores();

        drpProveedor.removeAllItems();

        for (Proveedor proveedor : proveedores) {
            drpProveedor.addItem(proveedor.getDescripcion());
        }
    }

    private void actualizarTabla() {
        modeloTabla.setRowCount(0);

        for (Proveedor proveedor : BLLProveedor.obtenerTodosLosProveedores()) {
            Object[] fila = {proveedor.getIdProveedor(), proveedor.getDescripcion()};
            modeloTabla.addRow(fila);
        }
    }

