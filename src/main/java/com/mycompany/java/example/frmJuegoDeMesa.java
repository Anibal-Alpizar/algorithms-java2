    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        String nombre = txtNombre.getText();
        String categoriaInteraccion = (String) drpCategoriaInteraccion.getSelectedItem();
        String categoriaAccesorios = (String) drpCategoriaAccesorios.getSelectedItem();
        String mecanica = (String) drpMecanica.getSelectedItem();
        String numeroParticipantes = txtNumeroParticipantes.getText();
        int edadMinima = Integer.parseInt(txtEdadMinima.getText());

        String proveedor = drpProveedor.getSelectedItem().toString();

        System.out.println(proveedor);

        JuegoDeMesa juegoDeMesa = new JuegoDeMesa(nombre, categoriaInteraccion, categoriaAccesorios,
                mecanica, numeroParticipantes, edadMinima, proveedor);

        BLLJuegoDeMesa.agregarJuegoDeMesa(juegoDeMesa);

        JOptionPane.showMessageDialog(this, "El juego se ha agregado correctamente.", "Juego Agregado", JOptionPane.INFORMATION_MESSAGE);

        limpiarCampos();


    }//GEN-LAST:event_btnAgregarActionPerformed

