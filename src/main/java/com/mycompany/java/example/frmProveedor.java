    private void actualizarTabla() {
        modeloTabla.setRowCount(0);

        for (Proveedor proveedor : BLLProveedor.obtenerTodosLosProveedores()) {
            Object[] fila = {proveedor.getIdProveedor(), proveedor.getDescripcion()};
            modeloTabla.addRow(fila);
        }
    }

